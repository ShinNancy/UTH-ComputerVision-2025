"""HIỂU RÕ HƠN VỀ CẤU TRÚC MA TRẬN ẢNH MÀU"""

# Ví dụ: Giả sử chúng ta có một bức ảnh RGB nhỏ, kích thước 3×3 pixels
# Dữ liệu của ảnh có thể được biểu diễn dưới dạng một mảng NumPy 3D như sau:

"""1. BIỂU DIỄN MA TRẬN ẢNH"""

import numpy as np

# Tạo một ma trận 3x3 pixel với 3 kênh màu (RGB)
image = np.array([
    [[255, 0, 0], [0, 255, 0], [0, 0, 255]],  # Hàng 1: (Đỏ, Xanh lá, Xanh dương)
    [[255, 255, 0], [0, 255, 255], [255, 0, 255]],  # Hàng 2: (Vàng, Xanh lục, Hồng)
    [[0, 0, 0], [127, 127, 127], [255, 255, 255]]  # Hàng 3: (Đen, Xám, Trắng)
])

print("Kích thước của ma trận ảnh:", image.shape)  # (3, 3, 3)

"""
Ở đây:

image.shape = (3, 3, 3) nghĩa là ảnh có 3 hàng, 3 cột, và 3 kênh màu (RGB).
Mỗi pixel là một danh sách [R, G, B].
"""

"""2. CẤU TRÚC TỪNG PHẦN CỦA MA TRẬN"""

"""
╒═════════════╤═══════════════════╤═════════════╕
│Pixel        │ Giá trị RGB       │ Màu sắc     │
╞═════════════╪═══════════════════╪═════════════╡
│ (0,0)       │ [255, 0, 0]       │ Đỏ          │
├─────────────┼───────────────────┼─────────────┤
│ (0,1)       │ [0, 255, 0]       │ Xanh lá     │
├─────────────┼───────────────────┼─────────────┤
│ (0,2)       │ [0, 0, 255]       │ Xanh dương  │
├─────────────┼───────────────────┼─────────────┤
│ (1,0)       │ [255, 255, 0]     │ Vàng        │
├─────────────┼───────────────────┼─────────────┤
│ (1,1)       │ [0, 255, 255]     │ Xanh lục    │
├─────────────┼───────────────────┼─────────────┤
│ (1,2)       │ [255, 0, 255]     │ Hồng        │
├─────────────┼───────────────────┼─────────────┤
│ (2,0)       │ [0, 0, 0]         │ Đen         │
├─────────────┼───────────────────┼─────────────┤
│ (2,1)       │ [127, 127, 127]   │ Xám         │
├─────────────┼───────────────────┼─────────────┤
| (2,2)       │ [255, 255, 255]   │ Trắng       │
╘═════════════╧═══════════════════╧═════════════╛
"""

"""3. TRUY CẬP VÀ SỬA ĐỔI PIXEL"""
pixel_0_1 = image[0, 1]  # Lấy pixel tại hàng 0, cột 1
print("Pixel tại (0,1):", pixel_0_1)  # [0, 255, 0] (màu xanh lá)


